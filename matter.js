//////////////// Bar Quarter1 \\\\\\\\\\\\\\\\\\\\\
const pMatterYtdTwoYear = d3
  .selectAll('.ChartMatter-ytd')
  .append('svg')
  .attr('width', '180')
  .attr('height', 230);

const marginYtdMatter = {
  top: 20,
  right: 20,
  bottom: 20,
  left: 20,
};
const graphWidthYtdMatter =
  parseInt(
    pMatterYtdTwoYear.style('width')
  ) -
  marginYtdMatter.left -
  marginYtdMatter.right;
const graphHeightYtdMatter =
  parseInt(
    pMatterYtdTwoYear.style('height')
  ) -
  marginYtdMatter.top -
  marginYtdMatter.bottom;

//pembuatan group
const graphYtdMatter = pMatterYtdTwoYear
  .append('g')
  .attr('width', graphWidthYtdMatter)
  .attr('height', graphHeightYtdMatter)
  .attr(
    'transform',
    `translate(${marginYtdMatter.left},${marginYtdMatter.top})`
  );

const minYtdMatter = d3.min(
  matterDataYtdTwoYear,
  (d) => d.value
); //method untuk mendapatkan nilai terkecil, dg callback function

const maxYtdMatter = d3.max(
  matterDataYtdTwoYear,
  (d) => d.value
);

const xAxisGroupMatterYtdTwoYear =
  graphYtdMatter
    .append('g')
    .style('font-weight', 'bold')
    .style('color', '#98a0a8')
    .attr(
      'transform',
      `translate(-2, ${graphHeightYtdMatter})`
    );

const yAxisGroupMatterYtdTwoYear =
  graphYtdMatter.append('g');

const yMatterYtdTwoYear = d3
  .scaleLinear()
  .domain([
    -5,
    d3.max(
      matterDataYtdTwoYear,
      (d) => d.value
    ),
  ])
  .range([graphHeightYtdMatter, 0]);

const xMatterYtdTwoYear = d3
  .scaleBand()
  .domain(
    matterDataYtdTwoYear.map(
      (item) => item.name
    )
  )
  .range([0, graphWidthYtdMatter])
  .paddingInner(0.2)
  .paddingOuter(0.2);

const rectsMatterYtdTwoYear =
  graphYtdMatter
    .selectAll('rect')
    .data(matterDataYtdTwoYear);

rectsMatterYtdTwoYear
  .attr('width', 28)
  .attr(
    'height',
    (d) =>
      graphHeightYtdMatter -
      yMatterYtdTwoYear(d.value)
  )
  .attr('fill', greenBar)
  .attr('x', (d) =>
    xMatterYtdTwoYear(d.name)
  )
  .attr('y', (d) =>
    yMatterYtdTwoYear(d.value)
  );

rectsMatterYtdTwoYear
  .enter()
  .append('rect')
  .attr('width', 28)
  .attr(
    'height',
    (d) =>
      graphHeightYtdMatter -
      yMatterYtdTwoYear(d.value)
  )
  .attr('fill', (d) => {
    if (d.name == '2019') {
      return lightgrey;
    } else if (d.name == '2020') {
      return grey;
    } else {
      return 'darkblue';
    }
  })
  .attr('x', (d) =>
    xMatterYtdTwoYear(d.name)
  )
  .attr('y', (d) =>
    yMatterYtdTwoYear(d.value)
  );

rectsMatterYtdTwoYear
  .enter()
  .append('text')
  .style('font-size', '11px')
  .text((d) => `${d.value}`)
  .attr('x', (d) =>
    xMatterYtdTwoYear(d.name)
  )
  .attr(
    'y',
    (d) =>
      yMatterYtdTwoYear(d.value) - 5
  )
  .attr(
    'transform',
    `translate(${4},${0})`
  )
  .attr('text-anchor', 'start');

//create and call the axes
const xAxisMatterYtdTwoYear =
  d3.axisBottom(xMatterYtdTwoYear);

xAxisGroupMatterYtdTwoYear.call(
  xAxisMatterYtdTwoYear
);

xAxisGroupMatterYtdTwoYear
  .selectAll('line')
  .remove();

xAxisGroupMatterYtdTwoYear
  .select('.domain')
  .remove(); //remove path with class domain

console.log(xAxisGroupMatterYtdTwoYear);

